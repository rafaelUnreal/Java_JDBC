    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        DataSource dataSource = new DataSource();
        ClienteDAO clieDAO = new ClienteDAO(dataSource);
        ArrayList<Cliente> lista = clieDAO.readAll();
        if (lista != null){
            for (Cliente c : lista){
                jTextArea1.setText(jTextArea1.getText() + c.getNome() + "\n");
            }
        }
        dataSource.closeDataSource();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        DataSource dataSource = new DataSource();
        ClienteDAO clieDAO = new ClienteDAO(dataSource);
        Cliente cli = new Cliente();
        
        cli.setNome(jTextField1.getText());
        cli.setTelefone(jTextField2.getText());
        cli.setEmail(jTextField3.getText());
        
        clieDAO.insertCliente(cli);
        
        
        dataSource.closeDataSource();
    }//GEN-LAST:event_jButton2ActionPerformed

